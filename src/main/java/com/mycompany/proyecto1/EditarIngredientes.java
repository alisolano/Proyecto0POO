    private void EditarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditarBtnActionPerformed
        // Obtiene los valores ingresados por el usuario
        int codigoIngrediente = (int) jSpinner1.getValue();
        String nuevoNombre = jTextField1.getText();
        boolean disponible = jCheckBox1.isSelected();

        Recetario recetario = Recetario.getInstancia();
            
        if (nuevoNombre.isEmpty()) {
            JOptionPane.showMessageDialog(this, "El campo del nombre no puede estar en blanco.", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            // Llama al método para editar el ingrediente en el Recetario
            if (!recetario.existeIngrediente(codigoIngrediente)) {
                JOptionPane.showMessageDialog(this, "Ingrediente no encontrado", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
                recetario.editarIngrediente(codigoIngrediente, nuevoNombre, disponible);
                SuccessBox("Ingrediente editado con éxito", "");
                dispose(); // Cierra esta ventana únicamente, no el proyecto
            }
        }
    }//GEN-LAST:event_EditarBtnActionPerformed

